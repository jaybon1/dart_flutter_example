산술 연산자:
    더하기(+)
    빼기(-)
    곱하기(*)
    나누기(/)
    정수 나누기(~/)
    나머지(%)

비교 연산자:
    같음(==)같지 않음(!=)
    보다 큼(>)
    보다 작음(<)
    크거나 같음(> =)
    작거나 같음(<=)

논리 연산자:
    논리적 AND(&&)
    논리적 OR(||)
    논리적 NOT(!)

할당 연산자:
    단순 할당(=)
    복합 할당(+=, -=, *=, /=, %=, ~/=)

비트 연산자:
    비트 AND(&)
    비트 OR(|)
    비트 XOR(^)
    비트 NOT(~)
    왼쪽 시프트(<<)
    오른쪽 시프트(>>)
    제로 채우기 오른쪽 시프트(>>>)

조건 연산자:
    삼항 연산자(?:)

null 확인 연산자 (해당 변수가 null이면 ?? 뒤의 내용을 반환):
    ?? - exampleList ?? [1, 2, 3]   

타입 테스트 연산자:
    is 연산자
    is! 연산자

함수 연산자:
    () 연산자 - exampleFunc()

첨자 엑세스 연산자:
    [] 연산자  - exampleList[0]

조건부 멤버 엑세스 연산자:
    ?. 연산자 - exampleList?.add(1)

널 어설션 연산자 (null이 절대로 아니다 라는 확신을 가질 때 사용):
    ! 연산자 - exampleList!.add(1)

